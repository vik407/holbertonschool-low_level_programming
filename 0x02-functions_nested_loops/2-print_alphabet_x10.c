#include "holberton.h"
/**
 * print_alphabet_x10 - Entry point to the loops
 *
 * Description: Prints the alphabetx10 using a library followed by a new line.
 * Return: 0
 */
void print_alphabet_x10(void)
{

	char alphabet[26] = "abcdefghijklmnopqrstuvwxyz";
	int i = 0;
	int j;

	while (i < 10)
	{
		j = 0;
		while (j < 26)
		{
			_putchar(alphabet[j]);
			j++;
		}
		_putchar('\n');
		i++;
	}
}
